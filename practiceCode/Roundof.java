package practiceCode;

import java.util.*;

public class Roundof {
	public static void main(String[] args) {
		float a = 11.05f;
		System.out.println(Math.round(a));
		System.out.println(Math.max(11, 12));
		System.out.println(Math.min(11, 12));
		System.out.println(Math.ceil(11.12));
		System.out.println(Math.floor(11.12));
		System.out.println(Math.abs(-11.12));
		System.out.println(Math.PI);
	}

}
