package practiceCode;

public class CastingOfDataTypes {
	public static void main(String[] args) {
		int a = 12;
		double b = a;
		System.out.println(b);
		double c = 12.5;
		int d = (int) c;
		System.out.println(d);
		int e = ++a;
		int f = a++;
		System.out.println(e);
		System.out.println(f);
		System.out.println(a);
	}

}
